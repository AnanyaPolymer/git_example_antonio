def animate_text(text, speed=0.1, end='\n', big_text=False):
    if big_text:
        sys.stdout.write('\033[1m\033[4m')  # ANSI codes for bold and underline
    for char in text:
        sys.stdout.write(char.upper() if big_text else char)
        sys.stdout.flush()
        time.sleep(speed)
    print(end=end)
    if big_text:
        sys.stdout.write('\033[0m')  # ANSI code to reset text formatting

def countdown(seconds):
    for i in range(seconds, 0, -1):
        sys.stdout.write(f'\rCountdown: {i} ')
        sys.stdout.flush()
        time.sleep(1)
    print('\r', end='')

